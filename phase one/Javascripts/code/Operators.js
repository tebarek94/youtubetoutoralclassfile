// Arithmetic Operators:

let num1 = 13;

let num2 = 11;

//  addtion

const result = num1 + num2;
console.log(num1 + num2);
console.log(result);

// Subtraction)
console.log(num1 - num2);

// * (Multiplication)
console.log(num1 * num2);

// / (Division)
console.log(num1 / num2);

// % (Remainder/Modulo)
console.log(num1 % num2);

// ** (Exponentiation)
console.log(num1 ** num2);

// ++ (Increment)
console.log(++num2);

// -- (Decrement)
console.log(--num2);
